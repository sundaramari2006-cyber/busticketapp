const mongoose=require('mongoose')

const busSchema=new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    from: {
        type: String,
        required: true
    },
    to: {
        type: String,
        required: true
    },
    date: {
        type: String,
        required: true
    },
    time: {
        type: String,
        required: true
    }
})

const bus=mongoose.model('Bus', busSchema);

module.exports=bus